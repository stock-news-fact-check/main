# =========================
# Claim 숫자와 API 수치 비교
# =========================
def compare_claim_with_answer(
    claim: str,
    answer_result: Dict[str, Any],
    tolerance: float = 0.2
) -> Dict[str, Any]:
    """
    tolerance:
    - 퍼센트포인트 기준 허용 오차
    - 예: Claim 9.9%, API 10.0%면 차이 0.1 → TRUE
    """

    claim_numbers = extract_numbers_from_claim(claim)

    if not answer_result.get("success"):
        return {
            "fact_check_result": "UNKNOWN",
            "reason": answer_result.get("reason", "정답 수치 추출 실패"),
            "claim_numbers": claim_numbers,
            "api_numbers": []
        }

    api_numbers = []

    extracted = answer_result.get("extracted")

    if isinstance(extracted, dict):
        values = extracted.get("values", {})
        for v in values.values():
            if isinstance(v, (int, float)):
                api_numbers.append(v)

        if "has_disclosure" in extracted:
            return {
                "fact_check_result": "SUPPORTED" if extracted["has_disclosure"] else "NOT_SUPPORTED",
                "reason": "DART 공시 존재 여부를 기준으로 판단했습니다.",
                "claim_numbers": claim_numbers,
                "api_numbers": api_numbers,
                "evidence": extracted
            }

    elif isinstance(extracted, list):
        for item in extracted:
            for key in ["value", "current_amount", "previous_amount"]:
                if key in item and isinstance(item[key], (int, float)):
                    api_numbers.append(item[key])

    if not claim_numbers or not api_numbers:
        return {
            "fact_check_result": "UNKNOWN",
            "reason": "Claim 또는 API 응답에서 비교 가능한 숫자를 찾지 못했습니다.",
            "claim_numbers": claim_numbers,
            "api_numbers": api_numbers,
            "evidence": answer_result.get("extracted")
        }

    comparisons = []

    for claim_num in claim_numbers:
        closest_api_num = min(api_numbers, key=lambda x: abs(x - claim_num))
        diff = abs(claim_num - closest_api_num)

        comparisons.append({
            "claim_value": claim_num,
            "api_value": closest_api_num,
            "difference": diff,
            "matched": diff <= tolerance
        })

    matched_count = sum(1 for c in comparisons if c["matched"])

    if matched_count == len(comparisons):
        result = "TRUE"
        reason = "Claim의 모든 수치가 API 응답 수치와 허용 오차 내에서 일치합니다."

    elif matched_count > 0:
        result = "PARTLY_TRUE"
        reason = "Claim의 일부 수치만 API 응답 수치와 일치합니다."

    else:
        result = "FALSE"
        reason = "Claim의 수치가 API 응답 수치와 일치하지 않습니다."

    return {
        "fact_check_result": result,
        "reason": reason,
        "claim_numbers": claim_numbers,
        "api_numbers": api_numbers,
        "comparisons": comparisons,
        "evidence": answer_result.get("extracted")
    }


