def get_text_from_response(response):
    if hasattr(response, "content"):
        return response.content
    return str(response)


def safe_json_parse(response, original_claims=None):
    """
    JSON 파싱 실패해도 중간에 멈추지 않고 fallback 결과 반환
    """

    text = get_text_from_response(response)

    # 코드블록 제거
    text = re.sub(r"```json", "", text)
    text = re.sub(r"```", "", text)
    text = text.strip()

    # JSON 객체만 추출 시도
    json_match = re.search(r"\{.*\}", text, re.DOTALL)
    if json_match:
        json_text = json_match.group(0)
    else:
        json_text = text

    # 1차: 정상 JSON 파싱
    try:
        return json.loads(json_text)

    except json.JSONDecodeError as e:
        # print("[WARN] JSON Parsing Failed")
        # print("에러 위치:", e)
        # print("\n[LLM 원본 출력]")
        # print(text)
        print("\n" + "=" * 70)

    # 2차: trailing comma 제거 후 재시도
    fixed_text = re.sub(r",\s*}", "}", json_text)
    fixed_text = re.sub(r",\s*]", "]", fixed_text)

    try:
        return json.loads(fixed_text)

    except json.JSONDecodeError:
        # print("[WARN] JSON 복구 실패 → fallback 결과 생성")
        print("\n" + "=" * 70)

    # 3차: fallback 결과 반환
    fallback_claims = []

    if original_claims:
        for claim in original_claims:
            fallback_claims.append({
                "claim": claim,
                "company_name": "unknown",
                "stock_name": "unknown",
                "stock_code": "unknown",
                "market": "unknown",
                "date": "unknown",
                "period": "unknown",
                "api_target": "unknown",
                "metric": "unknown",
                "operator": "unknown",
                "value": "unknown",
                "unit": "unknown",
                "direction": "unknown",
                "verification_source": "unknown",
                "parse_status": "fallback"
            })

    return {
        "api_query_ready": False,
        "parse_success": False,
        "error_message": "LLM output could not be parsed as valid JSON",
        "claims": fallback_claims,
        "raw_output": text
    }

