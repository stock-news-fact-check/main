# 증감 방향 추출
# =========================
def extract_direction(claim: str) -> str:
    if any(word in claim for word in ["상승", "증가", "올랐다", "늘었다", "개선"]):
        return "UP"
    if any(word in claim for word in ["하락", "감소", "내렸다", "줄었다", "악화"]):
        return "DOWN"
    return "UNKNOWN"


# =========================
# 비교 유형 추출
# =========================
def extract_compare_type(claim: str) -> str:
    if "전일 대비" in claim:
        return "DAY_OVER_DAY"
    if "연초" in claim or "연초 대비" in claim:
        return "YEAR_TO_DATE"
    if "전년 대비" in claim or "지난해보다" in claim:
        return "YEAR_OVER_YEAR"
    if "전분기 대비" in claim:
        return "QUARTER_OVER_QUARTER"
    if "배 증가" in claim or "배 늘" in claim:
        return "MULTIPLE"
    return "DIRECT"


# =========================
# Claim 수치 추출 (개선)
# =========================
def extract_claim_numbers(claim: str) -> List[Dict[str, Any]]:
    """
    숫자와 그 맥락을 함께 추출하여 더 정확한 비교 가능
    예: "179.09포인트(2.29%)" -> [{"value": 179.09, "context": "포인트", ...}, ...]
    """
    pattern = r'([-+]?\d+(?:\.\d+)?)\s*([가-힣%]*)(?:\(([^)]+)\))?'
    matches = re.finditer(pattern, claim)

    numbers_with_context = []
    for match in matches:
        try:
            value = float(match.group(1))
            context = match.group(2)
            paren_content = match.group(3)

            numbers_with_context.append({
                "value": value,
                "context": context,
                "paren_content": paren_content,
                "position": match.start()
            })
        except (ValueError, TypeError):
            pass

    return numbers_with_context


# =========================
# 증감률 계산
# =========================
def calculate_change_rate(base_value: float, current_value: float) -> Optional[float]:
    """
    증감률 = (현재값 - 기준값) / 기준값 * 100
    """
    if base_value is None or current_value is None:
        return None

    if base_value == 0:
        return None

    return round(((current_value - base_value) / base_value) * 100, 2)


# =========================
# 배수 계산
# =========================
def calculate_multiple(base_value: float, current_value: float) -> Optional[float]:
    """
    배수 = 현재값 / 기준값
    """
    if base_value is None or current_value is None:
        return None

    if base_value == 0:
        return None

    return round(current_value / base_value, 2)


