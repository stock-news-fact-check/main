def execute_api(detail_result: Dict[str, Any]) -> Dict[str, Any]:

    api_target = detail_result.get("api_target")

    try:

        # -----------------
        # DART
        # -----------------
        if api_target == "DART":

            api_response = call_dart_api(detail_result)

        # -----------------
        # KRX
        # -----------------
        elif api_target == "KRX":

            api_response = call_krx_api(detail_result)

        # -----------------
        # PUBLIC DATA
        # -----------------
        elif api_target == "PUBLIC_DATA":

            api_response = call_public_data_api(detail_result)

        else:
            api_response = {
                "message": "호출 가능한 API가 없습니다."
            }

        return {
            "success": True,
            "api_target": api_target,
            "api_response": api_response
        }

    except Exception as e:

        return {
            "success": False,
            "api_target": api_target,
            "error": str(e)
        }


def run_factcheck_pipeline(claim: str):

    # ---------------------
    # 1차: Router
    # ---------------------
    router_prompt = build_router_prompt(claim)

    router_result = call_hcx(router_prompt, original_claims=[claim])

    next_prompt = router_result.get("next_prompt")

    # ---------------------
    # 2차: 전용 Prompt
    # ---------------------
    if next_prompt == "DART_PROMPT":

        detail_prompt = build_dart_prompt(claim)
        print(detail_prompt)

    elif next_prompt == "KRX_PROMPT":

        detail_prompt = build_krx_prompt(claim)
        print(detail_prompt)

    elif next_prompt == "PUBLIC_DATA_PROMPT":

        detail_prompt = build_public_data_prompt(claim)
        print(detail_prompt)

    else:

        return {
            "router_result": router_result,
            "detail_result": None,
            "api_result": None
        }

    detail_result = call_hcx(detail_prompt, original_claims=[claim])

    # ---------------------
    # 3차: 실제 API 호출
    # ---------------------
    api_result = execute_api(detail_result)

    return {
        "router_result": router_result,
        "detail_result": detail_result,
        "api_result": api_result
    }

