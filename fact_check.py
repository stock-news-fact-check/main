# 3. 공시명 매칭 점수 계산
# =========================
def calculate_report_match_score(
    report_name: str,
    keywords: List[str]
) -> Dict[str, Any]:

    matched_keywords = []

    for keyword in keywords:
        if keyword in report_name:
            matched_keywords.append(keyword)

    score = 0.0

    if keywords:
        score = round(len(matched_keywords) / len(keywords), 4)
        # print(score)

    return {
        "report_name": report_name,
        "matched_keywords": matched_keywords,
        "score": score
    }


# =========================
# 4. 날짜 범위 매칭
# =========================
def is_date_in_range(
    rcept_dt: Optional[str],
    start_date: Optional[str],
    end_date: Optional[str]
) -> bool:
    """
    rcept_dt, start_date, end_date는 YYYYMMDD 문자열 기준
    """

    if not rcept_dt:
        return False

    if start_date and rcept_dt < start_date:
        return False

    if end_date and rcept_dt > end_date:
        return False

    return True


# =========================
# 5. DART 공시검색 계산
# =========================
def calculate_dart_disclosure_search(
    claim: str,
    detail_result: Dict[str, Any],
    api_response: Dict[str, Any],
    min_score: float = 0.3
) -> Dict[str, Any]:
    """
    DART 공시검색 결과를 바탕으로 Claim을 검증하기 위한 계산 코드

    판단 기준:
    1. 공시 목록 존재 여부
    2. 공시명(report_nm)이 Claim 키워드와 일치하는지
    3. 접수일자(rcept_dt)가 조회 기간에 포함되는지
    """

    disclosures = normalize_dart_disclosure_list(api_response)

    entities = detail_result.get("entities", {})
    api_params = detail_result.get("api_params", {})

    start_date = (
        entities.get("start_date")
        or api_params.get("bgn_de")
    )
    end_date = (
        entities.get("end_date")
        or api_params.get("end_de")
    )

    llm_keywords = entities.get("keywords", [])
    rule_keywords = extract_disclosure_keywords_from_claim(claim)

    keywords = list(dict.fromkeys(llm_keywords + rule_keywords))

    matched_disclosures = []

    for row in disclosures:
        report_name = row.get("report_nm", "")
        rcept_dt = row.get("rcept_dt")

        score_result = calculate_report_match_score(
            report_name=report_name,
            keywords=keywords
        )

        date_matched = is_date_in_range(
            rcept_dt=rcept_dt,
            start_date=start_date,
            end_date=end_date
        )

        matched = (
            score_result["score"] >= min_score
            and date_matched
        )

        matched_disclosures.append({
            "corp_name": row.get("corp_name"),
            "corp_code": row.get("corp_code"),
            "stock_code": row.get("stock_code"),
            "report_name": report_name,
            "receipt_no": row.get("rcept_no"),
            "receipt_date": rcept_dt,
            "matched_keywords": score_result["matched_keywords"],
            "keyword_score": score_result["score"],
            "date_matched": date_matched,
            "matched": matched
        })

    final_matches = [
        item for item in matched_disclosures
        if item["matched"]
    ]

    if final_matches:
        fact_check_result = "SUPPORTED"
        reason = "Claim과 관련된 DART 공시가 조회 기간 내에서 확인되었습니다."
    elif disclosures:
        fact_check_result = "NOT_ENOUGH_EVIDENCE"
        reason = "DART 공시는 조회되었지만 Claim 키워드 또는 기간 조건과 충분히 일치하지 않습니다."
    else:
        fact_check_result = "NOT_SUPPORTED"
        reason = "조회 조건에 해당하는 DART 공시가 확인되지 않았습니다."

    return {
        "source": "DART",
        "calculation_type": "disclosure_search",
        "fact_check_result": fact_check_result,
        "reason": reason,
        "total_disclosure_count": len(disclosures),
        "matched_count": len(final_matches),
        "keywords": keywords,
        "date_range": {
            "start_date": start_date,
            "end_date": end_date
        },
        "matched_disclosures": final_matches[:10],
        "all_disclosure_candidates": matched_disclosures[:20]
    }

