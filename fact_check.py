# =========================
# Claim 수치와 계산값 비교
# =========================
def compare_calculated_values_with_claim(
    claim: str,
    calculated_result: Dict[str, Any],
    tolerance: float = 0.3
) -> Dict[str, Any]:

    claim_numbers = extract_claim_numbers(claim)
    compare_type = extract_compare_type(claim)
    direction = extract_direction(claim)

    values = calculated_result.get("values", [])

    if not claim_numbers:
        return {
            "fact_check_result": "UNKNOWN",
            "reason": "Claim에서 비교할 수치를 찾지 못했습니다.",
            "claim_numbers": [],
            "calculated_values": values
        }

    comparisons = []

    # 숫자들을 비율 변화 기준으로 정렬 (백분율이 포함된 것들)
    percentage_numbers = [
        n for n in claim_numbers
        if n.get("paren_content") and "%" in str(n.get("paren_content"))
    ]
    other_numbers = [n for n in claim_numbers if n not in percentage_numbers]

    # 계산값이 있는 항목들과 비교
    for idx, calculated_item in enumerate(values):
        if idx >= len(percentage_numbers):
            break

        claim_item = percentage_numbers[idx]
        claim_value = claim_item.get("value")

        if compare_type == "MULTIPLE":
            calculated_value = calculated_item.get("calculated_multiple")
            metric = "multiple"
        else:
            calculated_value = calculated_item.get("calculated_change_rate")
            metric = "change_rate"

        if calculated_value is None:
            comparisons.append({
                "target": calculated_item.get("target"),
                "claim_item": claim_item,
                "claim_value": claim_value,
                "calculated_value": None,
                "matched": False,
                "reason": "계산값이 없습니다."
            })
            continue

        # 방향 정규화
        if direction == "DOWN" and metric == "change_rate":
            normalized_claim_value = -abs(claim_value)
        elif direction == "UP" and metric == "change_rate":
            normalized_claim_value = abs(claim_value)
        else:
            normalized_claim_value = claim_value

        diff = abs(normalized_claim_value - calculated_value)

        comparisons.append({
            "target": calculated_item.get("target"),
            "metric": metric,
            "base_value": calculated_item.get("base_value"),
            "current_value": calculated_item.get("current_value"),
            "claim_item": claim_item,
            "claim_value": normalized_claim_value,
            "calculated_value": calculated_value,
            "difference": round(diff, 4),
            "matched": diff <= tolerance
        })

    # 모든 숫자 저장
    all_claim_values = [n.get("value") for n in claim_numbers]

    if not comparisons:
        return {
            "fact_check_result": "UNKNOWN",
            "reason": "API 응답에서 추출할 데이터가 부족하거나 Claim 수치와 비교할 계산값이 없습니다.",
            "claim_numbers": all_claim_values,
            "all_extracted_numbers": claim_numbers,
            "calculated_values": values,
            "debug": {
                "claim_count": len(claim_numbers),
                "values_count": len(values),
                "percentage_numbers": len(percentage_numbers)
            }
        }

    matched_count = sum(1 for c in comparisons if c["matched"])

    if matched_count == len(comparisons):
        result = "TRUE"
        reason = "기준값과 현재값으로 계산한 결과가 Claim 수치와 일치합니다."

    elif matched_count > 0:
        result = "PARTLY_TRUE"
        reason = "일부 항목만 Claim 수치와 일치합니다."

    else:
        result = "FALSE"
        reason = "계산한 증감률 또는 배수가 Claim 수치와 일치하지 않습니다."

    return {
        "fact_check_result": result,
        "reason": reason,
        "compare_type": compare_type,
        "direction": direction,
        "claim_numbers": all_claim_values,
        "all_extracted_numbers": claim_numbers,
        "comparisons": comparisons
    }


