def call_hcx(prompt: str, original_claims: List[str] = None, debug: bool = False) -> Dict[str, Any]:

    messages=[
        {"role": "system", "content": "너는 주식 뉴스 팩트체크 라우터다. 반드시 JSON으로만 답변한다."},
        {"role": "user", "content": prompt}
    ]

    chat = ChatClovaX(
        model="HCX-007",
        temperature=0,
        top_p=0.1,
        max_tokens=None,
        timeout=None,
        max_retries=2,
    )

    try:
        response = chat.invoke(messages)
    except Exception as e:
        print(f"[ERROR] HCX API 호출 실패: {str(e)}")
        if debug:
            print(f"프롬프트: {prompt[:200]}...")
        return {
            "api_query_ready": False,
            "parse_success": False,
            "error_message": f"HCX API 호출 실패: {str(e)}",
            "claims": [],
            "raw_output": str(e)
        }

    result = safe_json_parse(
        response=response,
        original_claims=original_claims
    )

    if not result:
        print("[ERROR] HCX 응답에서 content를 찾지 못했습니다")
        raise ValueError(f"HCX 응답에서 content를 찾지 못했습니다: {response}")

    return result


